numero = float(input('Digite um numero positivo  e inteiro : '))
numero1 = float(input('Digite outro numero positivo e inteiro : '))
print(' A soma do ',numero,'+',numero1,' é igual a ',numero + numero1)
print(' A subtração do ',numero,'-',numero1,' é igual a ',numero - numero1)
print(' A multiplicação do ',numero,'x',numero1,' é igual a ',numero * numero1)
print(' A divisão do ',numero,'/',numero1,' é igual a ', round(numero / numero1,2))
# o round serve para definir o numero de casas decimais
